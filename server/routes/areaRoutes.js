const express = require('express');
const router = express.Router();
const Area = require('../models/Area');
const Manager = require('../models/Manager');
const Shop = require('../models/Shop');
const crypto = require('crypto');

// GET all areas
router.get('/areas', async (req, res) => {
    try {
        const areas = await Area.find().sort({ createdAt: -1 });
        res.json(areas);
    } catch (err) {
        res.status(500).json({ error: err.message });
    }
});

// POST create area + auto create manager
router.post('/areas', async (req, res) => {
    try {
        const area = new Area(req.body);
        await area.save();

        const loginToken = crypto.randomBytes(32).toString('hex'); // Link ke liye zaruri hai
        const manager = new Manager({
            areaCode: area.areaCode,
            managerCode: area.areaCode + '-DEFAULT',
            name: area.areaName + ' Manager',
            email: `${area.areaCode.toLowerCase()}@autogen.local`,
            phone: req.body.phone || '',
            city: area.city,
            state: area.state, // ← FIX 1: State add kiya
            areaName: area.areaName,
            bucket: req.body.bucket || 'DEFAULT', // ← FIX 2: Bucket add kiya
            loginToken: loginToken,
            centerLat: area.centerLat,
            centerLng: area.centerLng,
            radius: area.radius,
            status: true, // ← FIX 3: Status add kiya
            autoGenerated: true
        });
        await manager.save();

        res.json({ success: true, area, manager });
    } catch (err) {
        res.status(400).json({ error: err.message });
    }
});

// PUT update area - Manager bhi update hoga
router.put('/areas/:id', async (req, res) => {
    try {
        const area = await Area.findByIdAndUpdate(req.params.id, req.body, { new: true });
        
        // Update manager with same areaCode
        await Manager.updateMany(
            { areaCode: area.areaCode },
            { 
                areaName: area.areaName,
                city: area.city,
                state: area.state, // ← FIX 4
                centerLat: area.centerLat,
                centerLng: area.centerLng,
                radius: area.radius,
                bucket: req.body.bucket || 'DEFAULT' // ← FIX 5
            }
        );
        
        res.json({ success: true, area });
    } catch (err) {
        res.status(400).json({ error: err.message });
    }
});

// DELETE area
router.delete('/areas/:id', async (req, res) => {
    try {
        const area = await Area.findById(req.params.id);
        if (!area) {
            return res.status(404).json({ error: 'Area not found' });
        }
        
        const managerCount = await Manager.countDocuments({ areaCode: area.areaCode });
        if (managerCount > 0) {
            return res.status(400).json({ error: 'Cannot delete. Managers exist in this area.' });
        }
        
        await Area.findByIdAndDelete(req.params.id);
        res.json({ success: true });
    } catch (err) {
        res.status(500).json({ error: err.message });
    }
});

