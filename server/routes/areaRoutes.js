// POST create shop - FIXED lat/lng conversion
router.post('/shops', async (req, res) => {
    try {
        const shopData = {
            ...req.body,
            location: {
                type: 'Point',
                coordinates: [req.body.lng, req.body.lat] // ← FIX 6: [lng, lat] format
            }
        };
        delete shopData.lat; // ← Extra fields hatao
        delete shopData.lng;
        
        const shop = new Shop(shopData);
        await shop.save();
        res.json({ success: true, shop });
    } catch (err) {
        res.status(400).json({ error: err.message });
    }
});

// PUT update shop - FIXED lat/lng conversion
router.put('/shops/:id', async (req, res) => {
    try {
        const updateData = { ...req.body };
        if (req.body.lat && req.body.lng) {
            updateData.location = {
                type: 'Point',
                coordinates: [req.body.lng, req.body.lat] // ← FIX 7
            };
            delete updateData.lat;
            delete updateData.lng;
        }
        const shop = await Shop.findByIdAndUpdate(req.params.id, updateData, { new: true });
        res.json({ success: true, shop });
    } catch (err) {
        res.status(400).json({ error: err.message });
    }
});

// DELETE shop
